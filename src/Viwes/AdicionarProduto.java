    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         Object selectedItem = produtoComboBox.getSelectedItem();
         Produtos produtoSelecionado = (Produtos) selectedItem;
          int valor = (int) quantidade.getValue();
         if(valor <= 0){
          JOptionPane.showMessageDialog(this, "Quantidede invÃ¡lida", "Favor digite uma quantidade vÃ¡lida", JOptionPane.WARNING_MESSAGE);
         }
         else if (produtoSelecionado.getQuantidadeDisponivel() < valor ){
          JOptionPane.showMessageDialog(this, "Estoque insuficiente", "Ops, parece que a nÃ£o temos essa quantidade :/", JOptionPane.WARNING_MESSAGE);
         }
         else{
         produtoSelecionado.setQuantidade(valor);
         
         Main.adicionarProduto(produtoSelecionado);
         }
   
    }//GEN-LAST:event_jButton1ActionPerformed

     
    
